Drill_GBu_Sprite.prototype.drill_sprite_updateHoverAndPress_Visible = function(){
	var data = this._drill_controller.drill_data();
	
	// > 隐藏全部子贴图（此处控制 子贴图的显示，与父贴图的显示无关）
	this._drill_childGIFSprite.visible = true;
	this._drill_childDeactiveSprite.visible = false;
	if( this._drill_childHoverSprite != undefined ){ this._drill_childHoverSprite.visible = false; }
	if( this._drill_childPressSprite != undefined ){ this._drill_childPressSprite.visible = false; }
	if( this._drill_childDeactiveHoverSprite != undefined ){ this._drill_childDeactiveHoverSprite.visible = false; }
	if( this._drill_childDeactivePressSprite != undefined ){ this._drill_childDeactivePressSprite.visible = false; }
	
	// > 激活
	if( this._drill_controller.drill_controller_isActive() == true ){
		
		// > 激活+高亮
		if( this._drill_controller.drill_GBu_isOnOnlyHover() == true ){	//（唯一悬停）
			
			// > 激活+高亮+按下
			if( this._drill_controller.drill_controller_isOnPress() == true ){
				
				if( this._drill_childPressSprite != undefined ){
					if( data['pushdown_mode'] == "贴图叠加" ){
						this._drill_childGIFSprite.visible = true;
						this._drill_childPressSprite.visible = true;
					}else if( data['pushdown_mode'] == "贴图切换" ){
						this._drill_childGIFSprite.visible = false;
						this._drill_childPressSprite.visible = true;
					}
				}
				
			// > 激活+高亮+不按下
			}else{
				
				if( this._drill_childHoverSprite != undefined ){
					if( data['highlight_mode'] == "贴图叠加" ){
						this._drill_childGIFSprite.visible = true;
						this._drill_childHoverSprite.visible = true;
					}else if( data['highlight_mode'] == "贴图切换" ){
						this._drill_childGIFSprite.visible = false;
						this._drill_childHoverSprite.visible = true;
					}
				}
				
			}
			
		// > 激活+不高亮
		}else{
			//（不操作）
		}
		
		
	// > 封印
	}else{
		
		// （封印时才载入资源）
		if( this._drill_sprite_deactiveBitmap == undefined ){
			this._drill_sprite_deactiveBitmap = ImageManager.loadBitmap( data['deactive_src_img_file'], data['deactive_src_img'], data['tint'], data['smooth'] );
			this._drill_childDeactiveSprite.bitmap = this._drill_sprite_deactiveBitmap;
		}
		this._drill_childDeactiveSprite.visible = true;
		if( data['deactive_mode'] == "贴图叠加" ){
			this._drill_childGIFSprite.visible = true;
		}else if( data['deactive_mode'] == "贴图切换" ){
			this._drill_childGIFSprite.visible = false;
		}
		
		// > 封印+高亮
		if( this._drill_controller.drill_GBu_isOnOnlyHover() == true ){	//（唯一悬停）
			
			// > 封印+高亮+按下
			if( this._drill_controller.drill_controller_isOnPress() == true ){
				
				if( this._drill_childDeactivePressSprite != undefined ){
					if( data['pushdownD_mode'] == "贴图叠加" ){
						this._drill_childDeactiveSprite.visible = true;
						this._drill_childDeactivePressSprite.visible = true;
					}else if( data['pushdownD_mode'] == "贴图切换" ){
						this._drill_childDeactiveSprite.visible = false;
						this._drill_childDeactivePressSprite.visible = true;
					}
				}
				
			// > 激活+高亮+不按下
			}else{
				
				if( this._drill_childDeactiveHoverSprite != undefined ){
					if( data['highlightD_mode'] == "贴图叠加" ){
						this._drill_childDeactiveSprite.visible = true;
						this._drill_childDeactiveHoverSprite.visible = true;
					}else if( data['highlightD_mode'] == "贴图切换" ){
						this._drill_childDeactiveSprite.visible = false;
						this._drill_childDeactiveHoverSprite.visible = true;
					}
				}
			}
			
		// > 封印+不高亮
		}else{
			//（不操作）
		}
			
		// > 封印时的高亮透明度
		if( data['highlightD_mode'] == "透明度切换" ){
			if( this._drill_controller.drill_GBu_isOnOnlyHover() == true ){	//（唯一悬停）
				this._drill_childDeactiveSprite.opacity = data['highlightD_opacity2'];
			}else{
				this._drill_childDeactiveSprite.opacity = data['highlightD_opacity1'];
			}
		}
	}
}
