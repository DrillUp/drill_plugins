//==============================
// * 按钮GIF子贴图 - 2C碰撞体-控制器用 - 刷新框架
//
//			说明：	> 因为 控制器-贴图 一对一，父贴图-子贴图 一对一，所以子贴图可以修改控制器的值。
//==============================
Drill_GBu_ChildGIFSprite.prototype.drill_GBu__refreshFrame = function(){
	var parent_sprite = this.parent.parent;	//（Drill_GBu_Sprite对象）
	
	// > 『贴图框架值归零』 - 未读取时不赋值
	if( this.bitmap == undefined ){
		parent_sprite._drill_controller._drill_collision_width = 0;
		parent_sprite._drill_controller._drill_collision_height = 0;
		return;
	}
	if( this.bitmap.isReady() == false ){
		parent_sprite._drill_controller._drill_collision_width = 0;
		parent_sprite._drill_controller._drill_collision_height = 0;
		return;
	}
	
	// > 『贴图框架值归零』 - 不接受宽度为0的标记
	if( this._realFrame.width == 0  ){
		parent_sprite._drill_controller._drill_collision_width = 0;
		parent_sprite._drill_controller._drill_collision_height = 0;
		return;
	}
	if( this._realFrame.height == 0 ){
		parent_sprite._drill_controller._drill_collision_width = 0;
		parent_sprite._drill_controller._drill_collision_height = 0;
		return;
	}
	
	// > 刷新框架
	parent_sprite._drill_controller._drill_collision_width = this._realFrame.width;
	parent_sprite._drill_controller._drill_collision_height = this._realFrame.height;
};



