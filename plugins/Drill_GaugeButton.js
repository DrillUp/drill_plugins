//==============================
// * E延迟指令 - 帧刷新 - 时间流逝
//
//			说明：	> 此处的时间流逝不会因为 暂停 而停止流逝。
//==============================
Drill_GBu_Controller.prototype.drill_controller_updateDelayingCommandImportant = function(){
	if( this._drill_curDelayingCommandTank.length == 0 ){ return; }
	
	// > 帧刷新 时间流逝
	for(var i = 0; i < this._drill_curDelayingCommandTank.length; i++ ){
		var dc_data = this._drill_curDelayingCommandTank[i];
		
		// > 时间-1
		dc_data['left_time'] -= 1;
		
	}
	
	// > 执行延迟指令（暂停/继续）
	for(var i = 0; i < this._drill_curDelayingCommandTank.length; i++ ){
		var dc_data = this._drill_curDelayingCommandTank[i];
		if( dc_data['left_time'] < 0 ){
			var method = dc_data['method'];
			var paramList = dc_data['paramList'];
			
			if( method == Drill_GBu_Controller.METHOD__drill_controller_setPause ){
				this.drill_controller_setPause( paramList[0] );
			}else if( method == Drill_GBu_Controller.METHOD__drill_controller_periodizeCommand_setPause ){  //暂停周期指令(延迟)
				this.drill_controller_periodizeCommand_setPause( paramList[0] );
			}else if( method == Drill_GBu_Controller.METHOD__drill_controller_periodizeCommand_clear ){  //清空周期指令(延迟)
				this.drill_controller_periodizeCommand_clear();
			}
		}
	}
}
//==============================
// * E延迟指令 - 帧刷新 - 执行延迟指令
//==============================
Drill_GBu_Controller.prototype.drill_controller_updateDelayingCommand = function(){
	if( this._drill_curDelayingCommandTank.length == 0 ){ return; }
	
	// > 执行延迟指令
	for(var i = 0; i < this._drill_curDelayingCommandTank.length; i++ ){
		var dc_data = this._drill_curDelayingCommandTank[i];
		if( dc_data['left_time'] < 0 ){
			var method = dc_data['method'];
			var paramList = dc_data['paramList'];
			
			switch( method ){
				case Drill_GBu_Controller.METHOD__drill_controller_setVisible:
					this.drill_controller_setVisible( paramList[0] );
					break;
				
				case Drill_GBu_Controller.METHOD__drill_controller_setBlendMode:
					this.drill_controller_setBlendMode( paramList[0] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_setLayerIndex:
					this.drill_controller_setLayerIndex( paramList[0] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_setZIndex:
					this.drill_controller_setZIndex( paramList[0] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_restoreBlendMode:
					this.drill_controller_restoreBlendMode();
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_restoreLayerIndex:
					this.drill_controller_restoreLayerIndex();
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_restoreZIndex:
					this.drill_controller_restoreZIndex();
					break;
				
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setOpacity:
					this.drill_controller_commandChange_setOpacity( paramList[0], paramList[1], paramList[2] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setRotate:
					this.drill_controller_commandChange_setRotate( paramList[0], paramList[1], paramList[2] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setRotateSpeed:
					this.drill_controller_commandChange_setRotateSpeed( paramList[0], paramList[1], paramList[2] );
					break;
					
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setScaleX:
					this.drill_controller_commandChange_setScaleX( paramList[0], paramList[1], paramList[2] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setScaleY:
					this.drill_controller_commandChange_setScaleY( paramList[0], paramList[1], paramList[2] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setSkewX:
					this.drill_controller_commandChange_setSkewX( paramList[0], paramList[1], paramList[2] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setSkewY:
					this.drill_controller_commandChange_setSkewY( paramList[0], paramList[1], paramList[2] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_restoreAttr:
					this.drill_controller_commandChange_restoreAttr();
					break;
				
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setMove:
					this.drill_controller_commandChange_setMove( paramList[0], paramList[1], paramList[2], paramList[3] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_commandChange_restoreMove:
					this.drill_controller_commandChange_restoreMove();
					break;
				
				case Drill_GBu_Controller.METHOD__drill_controller_GIF_setLocked:
					this.drill_controller_GIF_setLocked( paramList[0] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_GIF_setOncePlay:
					this.drill_controller_GIF_setOncePlay( paramList[0] );
					break;
				case Drill_GBu_Controller.METHOD__drill_controller_GIF_setFrame:
					this.drill_controller_GIF_setFrame( paramList[0] );
					break;
				
				default:
					break;
			}
		}
	}
	
	// > 销毁延迟指令
	for(var i = this._drill_curDelayingCommandTank.length-1; i >= 0; i-- ){
		var dc_data = this._drill_curDelayingCommandTank[i];
		if( dc_data['left_time'] < 0 ){
			this._drill_curDelayingCommandTank.splice( i, 1 );
		}
	}
}
