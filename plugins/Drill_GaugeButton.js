Drill_GBu_Controller.prototype.drill_controller_setDelayingCommand = function( method, paramList, delay_time ){
	var enable = false;
	switch( method ){
		case Drill_GBu_Controller.METHOD__drill_controller_setVisible:
		case Drill_GBu_Controller.METHOD__drill_controller_setPause:
		case Drill_GBu_Controller.METHOD__drill_controller_periodizeCommand_setPause:
		case Drill_GBu_Controller.METHOD__drill_controller_periodizeCommand_clear:
		
		case Drill_GBu_Controller.METHOD__drill_controller_setBlendMode:
		case Drill_GBu_Controller.METHOD__drill_controller_setLayerIndex:
		case Drill_GBu_Controller.METHOD__drill_controller_setZIndex:
		case Drill_GBu_Controller.METHOD__drill_controller_restoreBlendMode:
		case Drill_GBu_Controller.METHOD__drill_controller_restoreLayerIndex:
		case Drill_GBu_Controller.METHOD__drill_controller_restoreZIndex:
		
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setOpacity:
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setRotate:
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setRotateSpeed:
		
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setScaleX:
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setScaleY:
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setSkewX:
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setSkewY:
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_restoreAttr:
		
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_setMove:
		case Drill_GBu_Controller.METHOD__drill_controller_commandChange_restoreMove:
		
		case Drill_GBu_Controller.METHOD__drill_controller_GIF_setLocked:
		case Drill_GBu_Controller.METHOD__drill_controller_GIF_setOncePlay:
		case Drill_GBu_Controller.METHOD__drill_controller_GIF_setFrame:
			enable = true;
			break;
		
		default:
			break;
	}
	if( enable == false ){ return; }
	
	var dc_data = {};
	dc_data['method'] = method;
	dc_data['paramList'] = paramList;
	dc_data['left_time'] = delay_time;
	this._drill_curDelayingCommandTank.push( dc_data );
}
