//==============================
// * DEBUG悬停范围 - 帧刷新 绘制范围
//==============================
Scene_Map.prototype.drill_GBu_updateDrawRangeBitmap = function() {
	if( this._drill_GBu_DebugSprite == undefined ){ return; }
	
	// > 清空绘制
	var temp_bitmap = this._drill_GBu_DebugSprite.bitmap;
	temp_bitmap.clear();
	
	// > 控制器遍历
	for(var i = 0; i < $gameSystem._drill_GBu_dataTank_curController.length; i++ ){
		var controller = $gameSystem._drill_GBu_dataTank_curController[i];
		if( controller == undefined ){ continue; }
		if( controller.drill_controller_isVisible() == false ){ continue; }
		
		
		// > 判断悬停
		var is_hover = controller.drill_controller_isOnHover();
		
		
		// > 绘制 - 颜色标记
		var color_line = "rgb(100,180,225)";
		var color_line_pixel = "rgb(100,180,225,0.13)";
		var color_text = "rgb(100,180,225)";
		var color_background = "rgba(100,180,225,0.225)";
		if( is_hover == true ){
			color_line = "rgb(0,255,0)";
			color_line_pixel = "rgb(0,255,0,0.13)";
			color_text = "rgb(0,255,0)";
			color_background = "rgba(0,255,0,0.225)";
		}
		
		// > 绘制 - 获取矩形的四个顶点
		var point_list = controller.drill_controller_getRectPoint();
		if( point_list == null ){ continue; }
		
		// > 绘制 - 绘制凸多边形
		if( data['pixelHoverEnabled'] == true ){
			temp_bitmap.drill_GBu_drawPolygon( point_list, color_background, color_line_pixel, 24, "round" );
		}else{
			temp_bitmap.drill_GBu_drawPolygon( point_list, color_background, color_line, 2, "miter" );
		}
		
		// > 绘制 - ID编号
		var painter = temp_bitmap._context;
        painter.save();										//（a.存储上一个画笔状态）
        painter.font = temp_bitmap._makeFontNameText();		//（b.设置样式）
		painter.fillStyle = color_text;
		painter.strokeStyle = "rgba(0,0,0,0.7)";
		painter.lineWidth = 4;
		painter.lineJoin = 'round';
		painter.strokeText( String(i), 						//（c.路径填充/描边，fillText）
			point_list[0].x+10, point_list[0].y+30, 60 );
		painter.fillText( String(i), 				
			point_list[0].x+10, point_list[0].y+30, 60 );
		painter.restore();									//（d.回滚上一个画笔状态）
		
		// > 绘制 - 矩形中心点
		temp_bitmap.drawCircle( controller.drill_controller_finalTransform_x(), controller.drill_controller_finalTransform_y(), 9, color_line );
		temp_bitmap.drawCircle( controller.drill_controller_finalTransform_x(), controller.drill_controller_finalTransform_y(), 5, "#ff0000" );
	}
}
//==============================
// * DEBUG悬停范围 - 几何绘制 - 填充+描边多边形
//			
//			参数：	> fill_color 字符串   （填充颜色）
//					> stroke_color 字符串 （描边颜色）
//					> lineWidth 数字      （线宽）
//					> lineJoin 字符串     （连接处，包含miter/round/bevel 尖角/圆角/斜角，默认miter）
