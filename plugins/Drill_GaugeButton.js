Scene_Map.prototype.drill_GBu_create = function(){
	$gameTemp._drill_GBu_spriteTank = [];			//贴图容器（不允许出现null值）
	
	for(var i=0; i< $gameSystem._drill_GBu_dataTank_curController.length; i++){
		var temp_controller = $gameSystem._drill_GBu_dataTank_curController[i];
		if( temp_controller == undefined ){ continue; }
		
		
		// > 创建贴图
		var temp_sprite = new Drill_GBu_Sprite();
		temp_sprite.drill_sprite_setController( temp_controller );
		temp_sprite.drill_sprite_initChild();
		
		
		// > 添加贴图到层级
		$gameTemp._drill_GBu_spriteTank.push( temp_sprite );
		this.drill_GBu_layerAddSprite( temp_sprite, temp_controller._drill_layerIndex );
	}
	
	// > 层级排序
	this.drill_GBu_sortByZIndex();
	
	
	// > 开始播放（2B自移动效果）
	for(var i=0; i< $gameSystem._drill_GBu_dataTank_curController.length; i++){
		var temp_controller = $gameSystem._drill_GBu_dataTank_curController[i];
		if( temp_controller == undefined ){ continue; }
		temp_controller.drill_controller_playSlideAnim();
	}
}
